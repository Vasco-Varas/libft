#include "../../includes/libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t		i;
	size_t		o;

	o = 0;
	i = ft_strlen(s1);
	while (s2[o] && o < n)
	{
		s1[i + o] = s2[o];
		o++;
	}
	s1[i + o] = '\0';
	return (s1);
}
